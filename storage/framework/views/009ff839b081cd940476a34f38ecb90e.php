    <div class="texto">
        <p>O diagnóstico da HAS deve ser baseado em pelo menos duas aferições de PA por consulta, em pelo menos duas
            consultas.</p>
        <p>HAS crônica: condição clínica multifatorial, geralmente não associada a sintomas, caracterizada por elevação
            sustentada dos níveis pressóricos sistólicos ≥ 140 mmHg e/ou diastólicos ≥ 90 mmHg.</p>
        <p>Sempre que possível, o diagnóstico de HAS deve ser estabelecido em mais de uma visita médica. Geralmente de 2
            a 3 visitas com intervalos de 1 a 4 semanas (dependendo do nível de pressão). O diagnóstico é feito em uma
            única visita se a PA do paciente estiver maior ou igual a 180/110 mmHg e houver evidência de doença
            cardiovascular.</p>
        <p>Sempre que possível, incluir a medição da PA fora do consultório tanto para diagnóstico, quanto para
            pacientes com PA elevada no consultório mesmo com tratamento otimizado.</p>
        <br>
            <p><h5>Crise hipertensiva:</h5>Pacientes com ou sem diagnóstico prévio de HAS podem apresentar episódios agudos,
            geralmente com pressão arterial sistólica (PAS) superior a 180 mmHg e pressão arterial diastólica (PAD)
            superior a 120 mmHg.</p>
        <p>Podem ser sintomáticos dependendo do órgão primariamente acometido, estando o quadro clínico associado às
            síndromes vasculares agudas (infarto do miocárdio, acidente vascular encefálico isquêmico ou hemorrágico e
            síndromes aórticas) ou edema agudo de pulmão. Mais raramente associa-se a encefalopatia hipertensiva (com
            alterações principalmente na fundoscopia e manifestações neurológicas) e hipertensão maligna (igualmente com
            manifestações neurológicas e fundoscópicas, mas acompanhada de perda progressiva da função renal).</p>
        <br>
            <p><h5 class="atencao">Atenção: Nessas situações o tratamento deve ser com anti-hipertensivos de uso endovenoso.</h5></p>
        <br>
        <p><h5>Sinais e Sintomas dependem do órgão acometido</h5>
            - Sinais neurológicos focais
            <br>
            - Alterações visuais
            <br>
            - Congestão (edema agudo de pulmão)
            <br>
            - Dor torácica
            <br>
            - Insuficiência renal
            <br>
            - Insuficiência hepática
        </p>
        <br>
        <p><h5>Pseudocrise hipertensiva:</h5> aumento acentuado da PA mais frequentemente associado ao uso inadequado de
            anti-hipertensivos, não apresentando lesão aguda de órgão alvo.</p>
        <p>O tratamento anti-hipertensivo deve ser otimizado e as medidas não-farmacológicas reforçadas.</p>
